#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*c;

	i = 0;
	if (n)
	{
		c = s;
		while (i < n)
		{
			c[i] = '\0';
			i++;
		}
	}
}
