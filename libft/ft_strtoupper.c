#include "libft.h"

char	*ft_strtoupper(char *s)
{
	int		i;

	if (!s)
		return (NULL);
	i = -1;
	while (s[++i])
		if (97 <= s[i] && s[i] <= 122)
			s[i] -= 32;
	return (s);
}
